func (c *Client) GetPokemonData(id string) (PokemonRes, error) {
	path := fmt.Sprintf("pokemon/%s", id)
	key := baseURL + path
	body, err := c.cachedGetData(key)
	if err != nil {
		return PokemonRes{}, err
	}
	pokemon, err := parseJSON[PokemonRes](body)
	if err != nil {
		return PokemonRes{}, err
	}
	return pokemon, nil
}
